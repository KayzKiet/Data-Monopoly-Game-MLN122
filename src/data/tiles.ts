import type { Asset, Tile } from '../types/game';

const asset = (
  id: string,
  tileId: string,
  name: string,
  type: Asset['type'],
  era: Asset['era'],
  purchasePrice: number,
  baseRent: number,
  theoryConnection: string,
): Asset => ({
  id,
  name,
  tileId,
  type,
  era,
  purchasePrice,
  baseRent,
  upgradeCost: Math.round(purchasePrice * 0.45),
  level: 1,
  maxLevel: 3,
  lapsHeld: 0,
  theoryConnection,
});

const ownableTile = (
  index: number,
  id: string,
  name: string,
  type: Asset['type'],
  era: Asset['era'],
  purchasePrice: number,
  baseRent: number,
  description: string,
  theoryConnection: string,
): Tile => ({
  id,
  index,
  name,
  type,
  era,
  description,
  asset: asset(`asset-${id}`, id, name, type, era, purchasePrice, baseRent, theoryConnection),
});

